# Practica For i i range()

num_usr = int(input("Ingrese un valor: ")) #Guardamos el valor que quiere el usuario a la variable
resultado = 0 #Declaramos la variable:

for i in range(num_usr): #Iniciamos el bucle for desde 0 hasta el numero que nos de el usuario
    resultado += i #Hacemos la suma y la guardamos en la variable
print("La suma es:",resultado) #Mostramos el resultado de la suma



