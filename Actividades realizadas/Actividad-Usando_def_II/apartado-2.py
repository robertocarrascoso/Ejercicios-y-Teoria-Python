# 2. Crea una función presentar(nombre, edad) que reciba un nombre y una edad, y muestre un mensaje como: "Hola, mi nombre es Juan y tengo 25 años." Luego, pruébala con al menos tres valores diferentes. 

def presentar(nombre, edad):
    print(f"Hola, mi nombre es {nombre} y tengo {int(edad)} años.")

presentar("Roberto", 19)
presentar("Eric", 21)
presentar("Iker", 89)