# Crear un programa que realice una simple suma

# Declaramos las variables que vamos a utilizar
num_1 = 0
num_2 = 0
resultado = 0

# Solicitamos los valores a sumar al usuario
num_1 = int(input("Introduzca el primer número: "))
num_2 = int(input("Introduzca el segundo número: "))

# Realizamos la suma y el resultado lo guardamos en su variable
resultado = num_1 + num_2

# Creamos el mensaje indicando el resultado
print("La suma de", num_1, "y",num_2, "es", resultado)