# Programa simple que toma 2 números y realiza una suma

# Declaramos las variables que vamos a utilizar
primer_num = 0
segundo_num = 0
resultado = 0

# Escribimos calculadora tal y como pide el enunciado
print("Calculadora")

# Solicitamos los 2 primeros numeros y los introducimos en sus variables
primer_num = int(input("Introduzca el primer número: "))
segundo_num = int(input("Introduzca el segundo número: "))

# Hacemos la suma y la introducimos en variable
resultado = primer_num + segundo_num

# Mostramos el resultado
print("El resultado es:",resultado)